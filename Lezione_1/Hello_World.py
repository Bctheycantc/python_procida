# Cancelletto per commentare riga singola
print("Hello World")
# \n per andare a capo durante una stringa
print("Watch the sun rise \nAnd watch the moon fall")
# il comando sep, inserisce un separatore tra le stringhe
print("Manuel", "Paolo", "Christian", "Marco", sep=", ")
# differenti tipi di dato, corrispondamente: string, number (integer e float) e boolean
print("2", 2, 2.0) #string, integer e float

print(True) #boolean
print(False)

#operatori di confronto (matematico), producobo booelano
print(1 < 3) #true
print(3 > 5) #false

# operatori di confronto
print(1.5 + 2.8)
print(4.2 - 6.8)

#Modulo %, riporta come risulatato il resto della divisione
print(14 % 3) #14 / 3 = 4 con il resto di due
print(44 % 6)

# con " ** " calcolo la potenza
print(4 ** 6) 
print(2 ** 8)

# con " // " divide e arrotonda all'intero inferiore
print(8 // 3)
print(14 // 5)